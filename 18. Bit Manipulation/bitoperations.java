public class bitoperations {
    public static void main(String args[]) {
        System.out.println("Binary AND & Operation on (5&6) is "+(5&6));//4
        System.out.println("Binary OR | Operation on (5|6) is "+(5|6));//7
        System.out.println("Binary XOR ^ Operation on (5^6) is "+(5^6));//3
        System.out.println("Binary One's ~ complement Operation on (~5) is "+(~5));//-6
        System.out.println("Binary Left Shift Operator << on (5<<2) is "+(5<<2));//20
        System.out.println("Binary Right Shift Operator >> on (6>>1) is "+(6>>1));//3
    }
}
