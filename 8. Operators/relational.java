public class relational {
    public static void main(String args[]) {
        int a = 10;
        int b = 5;
        System.out.println("== Operator "+(a==b)); //false
        System.out.println("!= Operator "+(a!=b)); //true
        System.out.println("> Operator "+(a>b));  //true
        System.out.println("< Operator "+(a<b)); //false
        System.out.println(">= Operator "+(a>=b)); //true
        System.out.println("<= Operator "+(a<=b)); //false
    }
}
