public class switchstat {
    public static void main(String args[]) {
        int num = 2;
        switch(num) {
            case 1 : 
            System.out.println("Case 1 is true");
                    break;
            case 2 : 
            System.out.println("Case 2 is true");
                    break;
            case 3 : 
            System.out.println("Case 3 is true");
                    break;
            case 4 : 
            System.out.println("Case 4 is true");
                    break;
            default : 
            System.out.println("Default Case is true");
        }
    }
}// note in case these number 1 2 3 4 can be also random not in a sequence and also could be a string or char like "a"
